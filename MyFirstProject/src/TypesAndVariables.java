
public class TypesAndVariables {
	public static void main(String[] args) {
		int myVariable; //deklariram varijablu naziva myVariable koja sadrži tip podatka int
		myVariable = 5; //pokrećem myVariable s vrijednosti 5
		
		System.out.println(myVariable);
		
		myVariable = 10;
		
		System.out.println(myVariable);
		
		myVariable = myVariable + 1;
		System.out.println(myVariable);
	}

}
