
public class TypesAndVariables {
	public static void main(String[] args) {
		int myVariable; //deklariram varijablu naziva myVariable koja sadrži tip podatka int
		myVariable = 5; //pokrećem myVariable s vrijednosti 5
		
		int myVar2 = 7; //deklariram i pokrećem varijablu u jednoj liniji
		System.out.println(myVariable + myVar2);
		
		double x = 2.5;
		System.out.println(x);
		
		x = myVariable;
		System.out.println(x);
	}
	
}


